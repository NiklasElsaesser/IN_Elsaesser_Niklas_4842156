function y = myPoly(x)
%FUNCTION_NAME - subtracting 3 from x and squaring the result
%
% Syntax:  [y] = myPoly(x)
%
% Inputs:
%    x - The x-Axis Value for the according y-Axis value
%
% Outputs:
%    y - result of the function
%
% Example: 
%    y = (5-3)^2
%    y = 4
%
% Other m-files required: none
% Subfunctions: none
% MAT-files required: none
%
% See also: dmyPoly

% Author: Niklas Elsaesser
% email: inf20184@lehre.dhbw-stuttgart.de
% Website: https://github.com/NiklasElsaesser
% May 2022; Last revision: 30.03.2022

y = (x-3)^2;
end